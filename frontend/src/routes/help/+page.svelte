Remerciement

Laisser un message de remerciement dans les colis que vous envoyez.
Il peut être rédigé à la main pour plus de convivialité ou imprimé.

Cela permet de fidéliser le client en le laissant sur un message sympathique et un expérience personnalisée.

Il pourra s'abbonner à votre compte, racheter chez vous ou même parler de votre boutique à ses proches.

